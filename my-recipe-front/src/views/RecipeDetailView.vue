<script setup>
import { ref, onMounted, computed } from 'vue'
import { useRoute, useRouter } from 'vue-router'
import { recipeService } from '../services/recipe'
import { ElMessage } from 'element-plus'

const route = useRoute()
const router = useRouter()
const recipe = ref(null)
const loading = ref(true)
const error = ref(false)

// 评论相关状态
const isCommentsModalVisible = ref(false)
const newComment = ref('')
const showEmojiPicker = ref(false)
const replyTo = ref(null)
const comments = ref([])
const commentsLoading = ref(false)
const commentsPage = ref(1)
const hasMoreComments = ref(true)

// 表情符号列表
const emojis = ['😊', '😂', '❤️', '👍', '🎉', '🔥', '😋', '👏', '🙌', '🤔']

// 显示的评论数量
const displayedComments = computed(() => {
  return comments.value.slice(0, 2) || []
})

// 获取食谱详情
const fetchRecipeDetail = async () => {
  try {
    loading.value = true
    const id = Number(route.params.id)
    const recipeData = await recipeService.getRecipeDetail(id)
    recipe.value = recipeData
  } catch (error) {
    console.error('Failed to fetch recipe:', error)
    alert('获取菜谱详情失败，请重试')
  } finally {
    loading.value = false
  }
}

const goBack = () => window.history.back()

const toggleLike = () => {
  recipe.value.likes++
}

const showComments = () => {
  isCommentsModalVisible.value = true
  showEmojiPicker.value = false
  replyTo.value = null
}

const closeComments = () => {
  isCommentsModalVisible.value = false
  showEmojiPicker.value = false
  replyTo.value = null
}

const toggleEmojiPicker = () => {
  showEmojiPicker.value = !showEmojiPicker.value
}

const addEmoji = (emoji) => {
  newComment.value += emoji
}

const submitComment = async () => {
  if (!newComment.value.trim()) return

  try {
    const comment = await recipeService.createComment(
      Number(route.params.id), 
      newComment.value,
      replyTo.value?.id || null  // 添加 parent_id 参数
    )
    // 重新获取评论列表，确保显示最新数据
    await fetchComments()
    // 清空评论输入框
    newComment.value = ''
    // 关闭表情选择器
    showEmojiPicker.value = false
    // 清除回复状态
    replyTo.value = null
    // 显示成功提示
    ElMessage.success(replyTo.value ? '回复发表成功' : '评论发表成功')
  } catch (error) {
    console.error('发表评论失败:', error)
    ElMessage.error('发表评论失败，请稍后重试')
  }
}

const handleReply = (comment, isInModal) => {
  replyTo.value = comment
  newComment.value = ''  // 清空输入框
  if (!isInModal) {
    // 在预览中回复，需要打开弹出层
    isCommentsModalVisible.value = true
  }
  showEmojiPicker.value = false
  // 聚焦到输入框
  nextTick(() => {
    const input = document.querySelector('.comment-input')
    if (input) {
      input.focus()
    }
  })
}

const likeComment = (comment) => {
  comment.likes++
}

// 获取评论列表
const fetchComments = async (page = 1) => {
  try {
    commentsLoading.value = true
    const response = await recipeService.getRecipeComments(Number(route.params.id), page)
    if (page === 1) {
      comments.value = response.data
    } else {
      comments.value = [...comments.value, ...response.data]
    }
    hasMoreComments.value = response.current_page < response.last_page
    commentsPage.value = response.current_page
  } catch (error) {
    console.error('Failed to fetch comments:', error)
  } finally {
    commentsLoading.value = false
  }
}

// 格式化日期
const formatDate = (dateString) => {
  const date = new Date(dateString)
  const now = new Date()
  const diff = now.getTime() - date.getTime()
  
  const minutes = Math.floor(diff / 60000)
  const hours = Math.floor(minutes / 60)
  const days = Math.floor(hours / 24)
  
  if (minutes < 60) {
    return `${minutes} minutes ago`
  } else if (hours < 24) {
    return `${hours} hours ago`
  } else {
    return `${days} days ago`
  }
}

// 在组件挂载时获取评论
onMounted(() => {
  fetchRecipeDetail()
  fetchComments()
})

// 加载更多评论
const loadMoreComments = () => {
  if (!commentsLoading.value && hasMoreComments.value) {
    fetchComments(commentsPage.value + 1)
  }
}
</script>

